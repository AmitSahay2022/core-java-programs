package org.example.geometry;

public class Rectangle {
    private int length;
    private int bredth;

    public Rectangle(int length, int bredth) {
        this.length = length;
        this.bredth = bredth;
    }

    public int getLength() {
        return length;
    }

    public int getBredth() {
        return bredth;
    }
}
